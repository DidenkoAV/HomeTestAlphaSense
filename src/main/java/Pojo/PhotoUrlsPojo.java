package Pojo;

public class PhotoUrlsPojo {
    private String url;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Override
    public String toString() {
        return "PhotoUrlsPojo{" +
                "url='" + url + '\'' +
                '}';
    }
}
